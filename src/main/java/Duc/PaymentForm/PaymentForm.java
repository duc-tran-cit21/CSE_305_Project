    private void btnSavePaymentActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSavePaymentActionPerformed
        // TODO add your handling code here:
        if (isValid()) {
            PaymentDAO paymentDAO = new PaymentDAO();
            PaymentManager paymentManager = new PaymentManager();
            String name = txtName.getText();
            String money = txtMoney.getText();
            String insurance = getInsurance();
            String method = (String) cboPayby.getSelectedItem();
            String date = txtDate.getText();
            String time = txtTime.getText();
            String illness = txtIllness.getText();
            String medication = txtMedication.getText();
            String report = txaReport.getText();
            Payment payment = new Payment(name, money, insurance, method, date, time, illness, medication, report);
            paymentManager.addPayment(payment);
            paymentDAO.savePayment(paymentManager.getListPayment());
        }

    }// GEN-LAST:event_btnSavePaymentActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        if (!FlatLaf.isLafDark()) {
            EventQueue.invokeLater(() -> {
                FlatAnimatedLafChange.showSnapshot();
                FlatMacDarkLaf.setup();
                FlatLaf.updateUI();
                FlatAnimatedLafChange.hideSnapshotWithAnimation();
            });
        } else {
            EventQueue.invokeLater(() -> {
                FlatAnimatedLafChange.showSnapshot();
                FlatMacLightLaf.setup();
                FlatLaf.updateUI();
                FlatAnimatedLafChange.hideSnapshotWithAnimation();
            });
        }
    }// GEN-LAST:event_jButton2ActionPerformed

