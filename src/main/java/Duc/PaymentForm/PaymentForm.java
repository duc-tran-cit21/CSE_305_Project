    public String getInsurance() {
        String insurance;
        if (rdoYes.isSelected()) {
            insurance = "yes";
        } else {
            insurance = "no";
        }
        return insurance;
    }

    public boolean validation() {
        boolean isValid = false;
        if (txtMoney.getText().isEmpty() || txtIllness.getText().isEmpty() || txtMedication.getText().isEmpty()
                || txaReport.getText().isEmpty()) {
            {
                JOptionPane.showMessageDialog(this, "Please enter both username and password.");
                isValid = false;
            }
            if (!rdoYes.isSelected() || !rdoNo.isSelected()) {
                isValid = false;
            }
            isValid = true;
        }
        return isValid;
    }

